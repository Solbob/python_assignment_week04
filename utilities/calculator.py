def add(a, b):
    return a + b


def subtract(a, b):
    return a - b


def multiply(a, b):
    return a * b


def divide(a, b):
    return a / b


if __name__ == '__main__':
    print('add(5, 3) =', add(5, 3))
    print('subtract(10, 4) =', subtract(10, 4))
    print('multiply(7, 2) =', multiply(7, 2))
    print('divide(20, 5) =', divide(20, 5))
